    def _apply_chat_template(self, messages: List[Dict[str, str]]) -> str:
        """Apply chat template with thinking enabled for rubric evaluation"""
        return self.tokenizer.apply_chat_template(
            messages,
            add_generation_prompt=True,
            tokenize=False,
            enable_thinking=True,
        )

    def _build_messages(self, example: Dict[str, Any]) -> List[Dict[str, str]]:
        """Build formatted messages using template"""
        example = self._normalize_row(example)

        # Extract query from input
        query = ""
        if isinstance(example.get("input"), list) and len(example["input"]):
            query = example["input"][0].get("content", "")

        # Extract answer from output
        answer = ""
        context = ""
        if isinstance(example.get("output"), list) and len(example["output"]):
            answer_data = example["output"][0].get("answer", {})
            if isinstance(answer_data, dict):
                answer = answer_data.get("content", "")

        # Build prompt using template
        examples_text = "\n\n".join(self._get_examples())
        prompt = PointwiseTrainTemplate.format(
            desc="Evaluate the helpfulness of the answer to the query.",
            rubrics="Score from 0 (not helpful) to 4 (very helpful).",
            examples=examples_text,
            query=query,
            context=context,
            answer=answer,
            enable_thinking=False,
        )

        return [{"role": "user", "content": prompt}]

    def _extract_ground_truth(self, row_dict: Dict[str, Any]) -> str:
        """Extract ground truth for rubric evaluation"""
        row_dict = self._normalize_row(row_dict)
        try:
            output_data = row_dict.get("output", [])
            if output_data:
                answer_data = output_data[0].get("answer", {})
                if isinstance(answer_data, dict):
                    label_data = answer_data.get("label", {})
                    if isinstance(label_data, dict):
                        return str(label_data.get("helpfulness", "")) or str(
                            label_data,
                        )
        except Exception:
            pass
        return ""

    def _get_data_source(self, row_dict: Dict[str, Any]) -> str:
        """Get data source for rubric evaluation"""
        row_dict = self._normalize_row(row_dict)
        return row_dict.get("data_source", "helpsteer2")
